# for variable in iterable:
    # block of code to be executed for each item in the iterable

#di list
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
    print(fruit)

#di tuple
colors = ('red', 'green', 'blue')
for color in colors:
    print(color)

#di set/dictionary
numbers = {1, 2, 3, 4, 5}
for number in numbers:
    print(number)

#di string
name = 'John Doe'
for letter in name:
    print(letter)

